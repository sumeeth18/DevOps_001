#importing datetime from datetime 

from datetime import date, datetime

today=datetime.now() #now is an attribute for datetime method
#datetime.now() is a function with an extra attributes

print(today.day) #day is an attribute to get current date for that varibale

print(today.year) #printing current year for that attribute

print(today.month) #month 

print(today.hour) #hour

print(today.minute) #minute

print(today.second) # second

print(today.microsecond) #microsecond
